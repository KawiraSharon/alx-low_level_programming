#include <stdio.h>

/**
 * add - function sums two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: summation of the integers
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - function subtracts two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: subtraction of the integers
 */
int sub(int a, int b)
{
        return (a - b);
}

/**
 * mul - function multiplies two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: product of the integers
 */
int mul(int a, int b)
{
        return (a * b);
}

/**
 * div - function divides two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: division of the integers
 */
int div(int a, int b)
{
        return (a / b);
}

/**
 * mod - function gets modulus of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: remainder of the division of integers
 */
int mod(int a, int b)
{
        return (a % b);
}
